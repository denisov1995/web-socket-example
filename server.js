app.get("/api/messages", async (req, res) => {
  const { user1, user2 } = req.query;

  try {
    const messages = await db.all(
      `SELECT * FROM messages 
       WHERE (sender = ? AND receiver = ?) 
          OR (sender = ? AND receiver = ?)
       ORDER BY timestamp DESC
       LIMIT ?`,
      [user1, user2, user2, user1, MAX_HISTORY]
    );

    res.json(messages.reverse());
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∏—Å—Ç–æ—Ä–∏–∏:", err);
    res.status(500).json({ error: "–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∏—Å—Ç–æ—Ä–∏–∏" });
  }
});

// üìã –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–ø–∏—Å–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
const broadcastUsers = async () => {
  try {
    const allUsers = await db.all("SELECT username, avatar FROM users");
    const connectedUsernames = new Set();

    for (let [, socket] of io.of("/").sockets) {
      if (socket.username) {
        connectedUsernames.add(socket.username);
      }
    }

    for (let [, socket] of io.of("/").sockets) {
      const userList = await Promise.all(
        allUsers
          .filter((u) => u.username !== socket.username)
          .map(async (u) => {
            const lastMessage = await db.get(
              `SELECT text, sender 
               FROM messages 
               WHERE (sender = ? AND receiver = ?) 
                  OR (sender = ? AND receiver = ?)
               ORDER BY timestamp DESC 
               LIMIT 1`,
              [u.username, socket.username, socket.username, u.username]
            );

            return {
              username: u.username,
              avatar: u.avatar,
              online: connectedUsernames.has(u.username),
              lastText: lastMessage?.text || "",
              lastFrom: lastMessage?.sender || null,
            };
          })
      );

      socket.emit("users", userList);
    }
  } catch (err) {
    console.error("‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–±–æ—Ä–µ —Å–ø–∏—Å–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π:", err.message);
  }
};

// ‚ö° Socket.IO
io.on("connection", async (socket) => {
  const rawCookies = socket.handshake.headers.cookie || "";
  const parsedCookies = cookie.parse(rawCookies);
  let profile = null;

  try {
    profile = parsedCookies.profile ? JSON.parse(parsedCookies.profile) : null;
  } catch (e) {
    console.error("‚ùå Cookie –ø–∞—Ä—Å–∏–Ω–≥:", e.message);
  }

  if (!profile?.username) {
    socket.disconnect();
    return;
  }

  // –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏–∑ –ë–î
  const user = await db.get(
    "SELECT username, avatar FROM users WHERE username = ?",
    [profile.username]
  );

  if (!user) {
    socket.disconnect();
    return;
  }

  socket.username = user.username;
  socket.avatar = user.avatar;

  // –ó–∞–≥—Ä—É–∂–∞–µ–º –∏—Å—Ç–æ—Ä–∏—é —Å–æ–æ–±—â–µ–Ω–∏–π –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
  try {
    const userMessages = await db.all(
      `SELECT * FROM messages 
       WHERE sender = ? OR receiver = ?
       ORDER BY timestamp DESC
       LIMIT ?`,
      [socket.username, socket.username, MAX_HISTORY]
    );

    socket.emit("message history", userMessages.reverse());

    const publicHistory = await db.all(
      `SELECT * FROM messages 
       WHERE receiver = 'public'
       ORDER BY timestamp DESC
       LIMIT ?`,
      [MAX_HISTORY]
    );

    socket.emit("public history", publicHistory.reverse());
  } catch (err) {
    console.error("–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∏—Å—Ç–æ—Ä–∏–∏:", err);
  }

  socket.on("request users update", () => {
    broadcastUsers();
  });

  // üîÑ –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –≤—Å–µ–º
  broadcastUsers();

  socket.broadcast.emit("user connected", {
    userID: socket.id,
    username: socket.username,
  });

  socket.on("typing", ({ to }) => {
    for (let [id, s] of io.of("/").sockets) {
      if (s.username === to) {
        io.to(id).emit("typing", { from: socket.username });
        break;
      }
    }
  });

  socket.on("stop typing", ({ to }) => {
    for (let [id, s] of io.of("/").sockets) {
      if (s.username === to) {
        io.to(id).emit("stop typing", { from: socket.username });
        break;
      }
    }
  });

  // üí¨ –ü—Ä–∏–≤–∞—Ç–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è
  socket.on("private message", async ({ content, toUsername }) => {
    const message = {
      sender: socket.username,
      receiver: toUsername,
      text: content,
      avatar: socket.avatar,
      is_read: false,
    };

    try {
      await db.run(
        `INSERT INTO messages (sender, receiver, text, avatar, is_read)
         VALUES (?, ?, ?, ?, ?)`,
        [
          message.sender,
          message.receiver,
          message.text,
          message.avatar,
          message.is_read,
        ]
      );

      // –û—Ç–ø—Ä–∞–≤–∏—Ç—å –ø–æ–ª—É—á–∞—Ç–µ–ª—é, –µ—Å–ª–∏ –æ–Ω –æ–Ω–ª–∞–π–Ω
      for (let [id, s] of io.of("/").sockets) {
        if (s.username === toUsername) {
          io.to(id).emit("private message", {
            ...message,
            from: message.sender,
            to: message.receiver,
            isRead: message.is_read,
          });
          break;
        }
      }

      // –û—Ç–ø—Ä–∞–≤–∏—Ç—å —Å–∞–º–æ–º—É —Å–µ–±–µ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ
      socket.emit("private message", {
        ...message,
        from: message.sender,
        to: message.receiver,
        isRead: message.is_read,
      });
    } catch (err) {
      console.error("–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–æ–æ–±—â–µ–Ω–∏—è:", err);
    }

    broadcastUsers();
  });

  socket.on("public message", async (content) => {
    const message = {
      sender: socket.username,
      receiver: "public",
      text: content,
      avatar: socket.avatar,
    };

    try {
      await db.run(
        `INSERT INTO messages (sender, receiver, text, avatar)
         VALUES (?, ?, ?, ?)`,
        [message.sender, message.receiver, message.text, message.avatar]
      );

      io.emit("public message", {
        from: message.sender,
        text: message.text,
        avatar: message.avatar,
        timestamp: new Date().toISOString(),
      });
    } catch (err) {
      console.error("–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –ø—É–±–ª–∏—á–Ω–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è:", err);
    }
  });

  socket.on("private image", async ({ toUsername, image }) => {
    const message = {
      sender: socket.username,
      receiver: toUsername,
      image,
      avatar: socket.avatar,
      text: "[–ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ üì∑]",
      is_read: false,
    };

    try {
      await db.run(
        `INSERT INTO messages (sender, receiver,text, image, avatar, is_read)
         VALUES (?, ?, ?, ?, ?, ?)`,
        [
          message.sender,
          message.receiver,
          message.text,
          message.image,
          message.avatar,
          message.is_read,
        ]
      );

      // –û—Ç–ø—Ä–∞–≤–∫–∞ –ø–æ–ª—É—á–∞—Ç–µ–ª—é –∏ —Å–µ–±–µ
      for (let [id, s] of io.of("/").sockets) {
        if (s.username === toUsername) {
          io.to(id).emit("private image", {
            ...message,
            from: message.sender,
            to: message.receiver,
            isRead: message.is_read,

            timestamp: new Date().toISOString(),
          });
        }
      }

      socket.emit("private image", {
        ...message,
        from: message.sender,
        to: message.receiver,
        isRead: message.is_read,
        timestamp: new Date().toISOString(),
      });
      broadcastUsers();
    } catch (err) {
      console.error("–û—à–∏–±–∫–∞ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è:", err);
    }
  });

  socket.on("disconnect", () => {
    broadcastUsers();
    socket.broadcast.emit("user disconnected", socket.id);
  });
});
